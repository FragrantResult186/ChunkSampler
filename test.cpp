// Returns number of failures in the chunk heightmap
// chunkX/chunkZ are chunk coordinates (world coords = chunk * 16)
static int checkChunk(std::uint64_t seed, int chunkX, int chunkZ, const int expected[256], const char* testName) {
    mc::ChunkSampler cs(seed);
    int failures = 0;
    for (int z = 0; z < 16; z++) {
        for (int x = 0; x < 16; x++) {
            int got = cs.getHeight(chunkX * 16 + x, chunkZ * 16 + z);
            int exp = expected[z * 16 + x];
            if (got != exp) {
                std::printf("  FAIL [%s] x=%d z=%d: expected=%d got=%d\n", testName, x, z, exp, got);
                failures++;
            }
        }
    }
    return failures;
}

